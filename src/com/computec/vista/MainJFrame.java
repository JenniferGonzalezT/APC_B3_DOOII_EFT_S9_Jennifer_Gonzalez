    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        String rut = txtRutCliente.getText().trim();
        
        if (rut == null || rut.isBlank()) {
            JOptionPane.showMessageDialog(this, "El rut no puede estar vac√≠o.");
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "¬øEliminar cliente con RUT " + rut + "?", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                clienteControlador.eliminar(rut);
                JOptionPane.showMessageDialog(this, "Cliente eliminado correctamente.");
                limpiarCamposCliente();
            }
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

